    private static String QUERY_BASED_ON_USER_NAME="from WpUsers where userLogin like '%";
    private static String QUERY_BASED_ON_DISPLAY_NAME="from WpUsers where displayName like '%";
    
    private void runQueryBasedOnUserName() {
        executeHQLQuery(QUERY_BASED_ON_USER_NAME + this.UserNameTextField.getText() + "%'");   
    }
    
    private void runQueryBasedOnDisplayName() {
        executeHQLQuery(QUERY_BASED_ON_DISPLAY_NAME + this.DisplayNameTextField.getText() + "%'");
    }
    
    /*@pamela*/
    private void executeHQLQuery(String hql) {
        try {
            Session session = HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(hql);
            List resultList = q.list();
            displayResult(resultList);
            session.getTransaction().commit();
        } catch (HibernateException he) {
            he.printStackTrace();
        }
    }
    
    private void displayResult(List resultList) {
    Vector<String> tableHeaders = new Vector<String>();
    Vector tableData = new Vector();
    tableHeaders.add("Id"); 
    tableHeaders.add("DisplayName");
    tableHeaders.add("UserLogin");
    tableHeaders.add("UserEmail");

    for(Object o : resultList) {
        WpUsers user = (WpUsers)o;
        Vector<Object> oneRow = new Vector<Object>();
        oneRow.add(user.getId());
        oneRow.add(user.getDisplayName());
        oneRow.add(user.getUserLogin());
        oneRow.add(user.getUserEmail());
        tableData.add(oneRow);
    }
    resultTable.setModel(new DefaultTableModel(tableData, tableHeaders));
}
    
    

